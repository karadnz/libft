#include "libft.h"

void ft_lstclear(t_list **lst, void (*del)(void*))
{
    t_list	*temp;

	if (!del || !lst || !*lst)
		return ;
	while (lst && *lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del); //eger tempde tutmazsak next de gider //dangling pointer bak
		*lst = temp;
	}
}